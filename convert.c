int			ft_convert_to_nb(char *tab)
{
	int		nb;
	int		i;
	int		j;

	i = 0;
	nb = 0;
	j = 0;
	while (tab[i] && j < 16)
	{
		if (tab[i] != '\n')
		{
			nb *= 2;
			j++;
		}
		if (tab[i] == '#')
			nb++;
		i++;
	}
	return (nb);
}
